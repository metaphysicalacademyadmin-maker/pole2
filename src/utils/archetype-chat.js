// Архетип-діалог. LLM-промпт під підтверджений архетип гравця.
//
// Контракт парент-сторінки:
//   window.__POLE_LLM__.archetypeChat = async ({ archetype, message, history, intention })
//     → { text, tone? } | null
//
//   archetype = ARCHETYPES item ({ id, name, symbol, color, ... })
//   message  = поточне повідомлення гравця
//   history  = [{ role: 'user'|'archetype', text }] — контекст
//   intention = поточний намір сесії
//
// Принципи бекенд-промпту:
//   • Архетип говорить ВІД ПЕРШОЇ ОСОБИ («Я — Воїн у тобі»)
//   • Не радить ззовні, говорить як внутрішня частина гравця
//   • 1-3 речення на repліку
//   • Запитує більше ніж відповідає
//   • Не діагностує, не оцінює
//   • Fallback на crisis-detection + повернути null

const TIMEOUT_MS = 12000;

export function isArchetypeChatAvailable() {
  if (typeof window === 'undefined') return false;
  return !!(window.__POLE_LLM__?.archetypeChat);
}

export async function fetchArchetypeReply({ archetype, message, history, intention }) {
  if (!isArchetypeChatAvailable()) return null;
  if (!message?.trim()) return null;

  const api = window.__POLE_LLM__.archetypeChat;
  try {
    const result = await Promise.race([
      api({ archetype, message: message.trim(), history: history || [], intention }),
      new Promise((_, rej) => setTimeout(() => rej(new Error('timeout')), TIMEOUT_MS)),
    ]);
    if (!result?.text) return null;
    return { text: result.text, tone: result.tone || 'neutral' };
  } catch (e) {
    return null;
  }
}


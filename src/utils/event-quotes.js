/**
 * Отримати випадкову цитату для події.
 * @param {string} eventKey — 'level-complete-3', 'shadow-petal-complete' тощо
 * @returns {{ text, source } | null}
 */
export function quoteForEvent(eventKey) {
  const list = QUOTES[eventKey] || QUOTES.default;
  if (!list?.length) return null;
  return list[Math.floor(Math.random() * list.length)];
}

/**
 * Чи є цитата для цього ключа? (без default fallback)
 */
export function hasQuoteFor(eventKey) {
  return !!QUOTES[eventKey];
}

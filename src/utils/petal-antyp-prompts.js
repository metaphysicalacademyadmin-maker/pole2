// Антип-провокації для пелюсток.
// Контекстуальні до глибини відповіді — не загальне «що приховав?»,
// а конкретно за типом instinct-вибору.

const PROMPTS = {
  // Гравець обрав deep-варіант (готовий, чесний)
  deep: [
    'Це красиво звучить. Спробуй ще раз — без обгортки.',
    'Я тобі вірю. Майже. Тепер скажи без поетики.',
    'Це справді так? Або це версія яку добре показувати?',
    'Чесно — а коли востаннє це не було правдою?',
    'Гарно сказано. А якщо протилежне — теж правда?',
  ],
  // Гравець обрав mid (серединний, обережний)
  mid: [
    'А якщо протилежне — теж правда? Що тоді?',
    'Ти йдеш по середині. Що ти захищаєш цією серединою?',
    'Серединна відповідь — зручна. Куди тебе тягне насправді?',
    'Або одне, або інше — обери. Що звучить ближче зараз?',
    'Це як «нормально» у відповідь на «як справи». Що під цим?',
  ],
  // Гравець обрав shadow (тінь, чесно з темним)
  shadow: [
    'Молодець що бачиш. А що з цим зробиш — або зробить це з тобою?',
    'Сказав. А тепер: коли востаннє це шкодило тобі або іншим?',
    'Я бачу тінь. А де вона у тілі прямо зараз?',
    'Тінь побачена — пів-зцілення. Друга половина — що?',
    'Це не вирок. Це факт. Що цей факт хоче від тебе?',
  ],
  // Custom — гравець написав своє
  custom: [
    'Своїми словами — найцінніше. Тепер: що ти НЕ написав?',
    'Гарно. А тепер чесно — що ти ще боявся написати?',
    'Це вже глибше. Спробуй на 2 шари нижче.',
    'Між рядками тут є щось. Назви.',
    'Якби ти писав це другу — лишив би це слово?',
  ],
};

// Спеціальні провокації коли у custom-тексті є тіньові тригери
const SHADOW_KEYWORDS = [
  'не знаю', 'мабуть', 'можливо', 'іноді', 'як завжди',
  'нормально', 'зазвичай', 'буває', 'мабуть так',
];

const SHADOW_PROVOCATIONS = [
  'Ти захищаєшся словом. Якби тут не було «{kw}» — що залишилось?',
  'Я чую захист. Без «{kw}» — як це звучить?',
  'Це слово зігріває. Без нього — що оголено?',
];

/**
 * Підібрати провокацію Антипа під контекст відповіді гравця.
 * Повертає рядок-питання.
 */
export function pickAntypPrompt(instinct) {
  if (!instinct) return PROMPTS.mid[0];
  const seed = (instinct.choice || instinct.customText || '').length;

  // Якщо custom з тіньовим словом — спеціальна провокація
  if (instinct.customText) {
    const lower = instinct.customText.toLowerCase();
    const found = SHADOW_KEYWORDS.find((kw) => lower.includes(kw));
    if (found) {
      const tpl = SHADOW_PROVOCATIONS[seed % SHADOW_PROVOCATIONS.length];
      return tpl.replace('{kw}', found);
    }
    return PROMPTS.custom[seed % PROMPTS.custom.length];
  }

  // За depth
  const bucket = instinct.depth === 'shadow' ? 'shadow'
    : instinct.depth === 'deep' ? 'deep'
    : 'mid';
  const list = PROMPTS[bucket];
  return list[seed % list.length];
}

export const CHANNEL_BLOCKS = ['буддистський', 'вищий', 'магічний', 'магістровський', 'зороастризм'];

export function checkChannelsUnlock(resources) {
  return CHANNELS
    .filter((c) => (resources[c.unlock.resource] || 0) >= c.unlock.threshold)
    .map((c) => c.id);
}

export function findChannel(id) {
  return CHANNELS.find((c) => c.id === id);
}

export function channelsByBlock(block) {
  return CHANNELS.filter((c) => c.block === block);
}

export function encouragementFor(count) {
  if (count === 0) return ENCOURAGEMENT.none;
  if (count === 1) return ENCOURAGEMENT.one;
  if (count <= 3) return ENCOURAGEMENT.few;
  return ENCOURAGEMENT.many;
}
